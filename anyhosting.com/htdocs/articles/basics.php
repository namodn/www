    is your address on the web, like "www.example.com" for your website or 
    "robert@example.com" for your email.</p>

    <p>Once you have your name picked out, you'll need to register it with
    an accredited company that specializes in this, called a 
    <a href="http://en.wikipedia.org/wiki/Domain_name_registrar">registrar</a>.
    Registrars typically allow you to search to see if your name is taken.
    Price varies, but you should pay no more than $15/yr.
    </p>
   
